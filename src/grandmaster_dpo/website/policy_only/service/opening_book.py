def maybe_get_opening_book(
    *,
    gm_name: str,
    board: chess.Board,
    played_moves_uci: list[str] | None = None,
) -> OpeningBookSelection | None:
    gm_key = _canonical_gm_name(gm_name)
    ply_abs = 2 * (board.fullmove_number - 1) + (1 if board.turn == chess.BLACK else 0)

    if ply_abs == 0 and board.turn == chess.WHITE:
        white_weights = WHITE_FIRST_MOVE_WEIGHTS.get(gm_key)
        if not white_weights:
            return None
        return OpeningBookSelection(
            branch_name="white_first_move",
            probabilities=_normalize_book_weights(board, white_weights),
        )

    if ply_abs != 1 or board.turn != chess.BLACK:
        return None

    first_move = None
    if played_moves_uci:
        first_move = str(played_moves_uci[0] or "").strip()
    if not first_move or first_move not in TOP_TEN_WHITE_FIRST_MOVES:
        return None

    black_weights = BLACK_REPLY_WEIGHTS.get(gm_key, {}).get(first_move)
    if not black_weights:
        return None
    return OpeningBookSelection(
        branch_name=f"black_reply_after_{first_move}",
        probabilities=_normalize_book_weights(board, black_weights),
    )
