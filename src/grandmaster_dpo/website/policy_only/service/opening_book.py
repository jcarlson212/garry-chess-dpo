def _normalize_book_weights(
    board: chess.Board,
    preferred_weights: dict[str, float],
    *,
    floor_prob: float = OPENING_BOOK_FLOOR_PROB,
) -> dict[str, float]:
    legal_moves = [move.uci() for move in board.legal_moves]
    if not legal_moves:
        return {}

    legal_count = len(legal_moves)
    floor_total = floor_prob * float(legal_count)
    if floor_total >= 1.0:
        uniform = 1.0 / float(legal_count)
        return {move_uci: uniform for move_uci in legal_moves}

    filtered_weights = {
        move_uci: max(0.0, float(weight))
        for move_uci, weight in preferred_weights.items()
        if move_uci in legal_moves
    }
    preferred_total = sum(filtered_weights.values())
    if preferred_total <= 0.0:
        uniform = 1.0 / float(legal_count)
        return {move_uci: uniform for move_uci in legal_moves}

    remaining = 1.0 - floor_total
    probs = {move_uci: floor_prob for move_uci in legal_moves}
    for move_uci, weight in filtered_weights.items():
        probs[move_uci] += remaining * (weight / preferred_total)
    return probs


